// Helper function to get related products
export function getRelatedProducts(productId: string): RelatedProduct[] {
  // Get the current product
  const currentProduct = stoneGraniteSawBlades.find(product => product.id === productId);
  if (!currentProduct) return [];
  
  // Get 2-3 other products from the same category
  const relatedProducts = stoneGraniteSawBlades
    .filter(product => product.id !== productId)
    .slice(0, 2)
    .map(product => ({
      id: product.id,
      name: product.name,
      price: product.price,
      discount_price: product.discount_price,
      image: product.images[0],
      slug: product.slug
    }));
  
  // Add an accessory as related product
  relatedProducts.push({
    id: 'diamond-blade-cooling',
    name: 'מערכת קירור לדיסק יהלום',
    price: 280,
    image: '/placeholder.jpg',
    slug: '/products/accessories/cooling-systems/diamond-blade',
  });
  
  return relatedProducts;
}

